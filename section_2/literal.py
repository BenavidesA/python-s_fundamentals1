"""
This script demonstrates the difference between string and integer literals in Python.
A literal is a notation for representing a fixed value in source code. 
For example:
- String literals are enclosed in quotes (e.g., "123").
- Integer literals are written as numbers without quotes (e.g., 123).
The script includes:
1. Printing a string literal ("123"), which is treated as text.
2. Printing an integer literal (123), which is treated as a numeric value.
"""

# This prints the string "123". The value is treated as text and enclosed in quotes.
print("123")

# This prints the integer 123. The value is treated as a number and not enclosed in quotes.
print(123)



