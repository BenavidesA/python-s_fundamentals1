#integers
print(1234567890)
print(12345_67890) # This will print the number as is, but it may be truncated in some environments.
#this "_" is just to make it more readable, it is not a separator

#negative integers
print(-1234567890) #to print negative integers, just add a "-" sign before the number

#octal and hexadecimal numbers
print(0o123) #octal is base 8, so it can only contain digits 0 to 7
print(0x123) #hexadecimal is base 16, so it can contain digits 0 to 9 and letters A to F (or a to f)

