#an operator is a symbol that tells the compiler to perform specific mathematical or logical manipulations
# on operands
#operators are used to perform operations on variables and values
#for example + is an operator that adds two operands, however
#not all operators are simple, some operators are more complex and require more than one operand
#operators:
# +, -, *, /, **, //, %
#the union of values and operators is called an expression

#exponential operator
# **
print(2**3) # 2^3 = 8
print(2**4) # 2^4 = 16

#multiplication operator
# *
print(2*3) # 2*3 = 6
print(2*4) # 2*4 = 8

#division operator
# / is used to divide two numbers and return float value
print(2/3) # 2/3 = 0.6666666666666666
print(2/4) # 2/4 = 0.5

#floor division operator
# // is used to divide two numbers and return integer value
print(8//4) # 8//4 = 2
print(4//4) #  4//4 = 1

#residue operator
# % is used to divide two numbers and return the remainder of the division
print(8%4) # 8%4 = 0
print(14%4) # 14%4 = 2

#addition operator
# +
print(2+3) # 2+3 = 5

#subtraction operator
# -
print(2-3) # 2-3 = -1


